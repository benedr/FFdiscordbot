export function seriesBuckets() {
    const buckets = new Map();
    for (const model of Object.keys(SENSITIVITY_DB)) {
        const brand = model.split(" ")[0];
        const seriesToken = model.split(" ")[1] || "";
        const key = `${brand} ${seriesToken}`.trim();
        if (!buckets.has(key)) buckets.set(key, []);
        buckets.get(key).push(model);
    }
    return buckets;
}

export function normalize(text) {
    return text.toLowerCase().replace(/\s+/g, "").replace(/[-_]/g, "");
}

